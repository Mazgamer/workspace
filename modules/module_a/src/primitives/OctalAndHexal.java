package primitives;

public class OctalAndHexal {

    public static void main(String[] args) {

        System.out.println("000000017 to wartość " + 15);
        System.out.println("\tCzy na pewno? " + 000000017);
        System.out.println("0x000F to wartość" + 15);
        System.out.println("\tCzy na pewno? " + 0x000F);

        // Dla poniższych wartości dostarcz analogiczne fragmenty kodu
        //4
        //128
        //65536
        //397
        //-128
        //-2147483648
        //2147483648
    }
}
